/// Render text buffer (80x25 cells) to RGBA pixels (640x400) using the CP437 8x16 font.
fn render_text_to_rgba(text: &[u16], pixels: &mut Vec<u8>) {
    const COLS: usize = 80;
    const ROWS: usize = 25;
    const CHAR_W: usize = 8;
    const CHAR_H: usize = 16;
    const FB_W: usize = COLS * CHAR_W;  // 640
    const FB_H: usize = ROWS * CHAR_H;  // 400

    pixels.resize(FB_W * FB_H * 4, 0);

    for row in 0..ROWS {
        for col in 0..COLS {
            let idx = row * COLS + col;
            let cell = if idx < text.len() { text[idx] } else { 0x0720 };

            let ch = (cell & 0xFF) as usize;
            let attr = ((cell >> 8) & 0xFF) as usize;
            let fg = &VGA_PALETTE[attr & 0x0F];
            let bg = &VGA_PALETTE[(attr >> 4) & 0x0F];

            let glyph_base = ch * 16;

            for glyph_row in 0..CHAR_H {
                let bits = VGA_FONT_8X16[glyph_base + glyph_row];
                let py = row * CHAR_H + glyph_row;

                for bit in 0..CHAR_W {
                    let px = col * CHAR_W + bit;
                    let di = (py * FB_W + px) * 4;
                    let color = if bits & (0x80 >> bit) != 0 { fg } else { bg };
                    pixels[di]     = color[0];
                    pixels[di + 1] = color[1];
                    pixels[di + 2] = color[2];
                    pixels[di + 3] = color[3];
                }
            }
        }
    }
}
